package java_primeros_pasos;

public class Ejemplo_Condicionales {
	public static void main(String[] args) {

		int edad = 17;
		int persona = 2;

		if (edad >= 18 || persona >= 2) {
			System.out.println("Puede entrar");

		} else {
			System.out.println("No puedes ingresar, lo lamento");
		}

	}

}
